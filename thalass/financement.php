    <h3>Dispositif démissionnaire</h3>
    <p>Ce dispositif a été créé afin de permettre aux salariés souhaitant démissionner, pour se reconvertir, une sécurisation de leur parcours. Il permet, sous certaines conditions, de percevoir des indemnités d&rsquo;Aide de Retour l&rsquo;Emploi (ARE).</p>
    <p>Entré en vigueur au 1er novembre 2019, le dispositif démissionnaire s&rsquo;adresse à tous les salariés en CDI :
        <ul>
            <li>ayant au moins 5 ans d'activité continue chez un ou plusieurs employeurs,</li>
            <li>ayant un projet de reconversion défini et reconnu comme réel et sérieux.</li>
        </ul>
    </p>
    <p>Ce dispositif est accessible aux salariés en CDI du secteur privé qui justifient d&rsquo;une activité professionnelle de 5 ans au cours des 60 derniers mois, à la date de leur démission.</p>
    <p>L&rsquo;activité professionnelle prise en compte peut être à temps plein ou à temps partiel, mais ne doit pas comprendre de période d&rsquo;interruption, ni de congés sans solde, de congés sabbatiques ou de période de disponibilité.</p>

